-- ------------------------------------------------------------
--  BƯỚC 2: XOÁ SEQUENCE CŨ 기존 시퀀스 삭제 (없으면 건너뜀)
-- ------------------------------------------------------------
BEGIN EXECUTE IMMEDIATE 'DROP SEQUENCE SEQ_USERS';      EXCEPTION WHEN OTHERS THEN NULL; END;
/
BEGIN EXECUTE IMMEDIATE 'DROP SEQUENCE SEQ_CATEGORIES'; EXCEPTION WHEN OTHERS THEN NULL; END;
/
