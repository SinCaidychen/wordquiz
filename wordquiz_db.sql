/
BEGIN EXECUTE IMMEDIATE 'DROP SEQUENCE SEQ_SESSION';    EXCEPTION WHEN OTHERS THEN NULL; END;
/
BEGIN EXECUTE IMMEDIATE 'DROP SEQUENCE SEQ_RANK';       EXCEPTION WHEN OTHERS THEN NULL; END;
/
 
-- ------------------------------------------------------------
--  BƯỚC 3: TẠO SEQUENCE 시퀀스 생성
-- ------------------------------------------------------------
CREATE SEQUENCE SEQ_USERS      START WITH 1 INCREMENT BY 1 NOCACHE NOCYCLE;
CREATE SEQUENCE SEQ_CATEGORIES START WITH 1 INCREMENT BY 1 NOCACHE NOCYCLE;
CREATE SEQUENCE SEQ_WORDS      START WITH 1 INCREMENT BY 1 NOCACHE NOCYCLE;
CREATE SEQUENCE SEQ_SESSION    START WITH 1 INCREMENT BY 1 NOCACHE NOCYCLE;
CREATE SEQUENCE SEQ_RANK       START WITH 1 INCREMENT BY 1 NOCACHE NOCYCLE;
 
-- ------------------------------------------------------------
--  BƯỚC 4: TẠO BẢNG 테이블 생성
-- ------------------------------------------------------------
CREATE TABLE USERS (
    USER_ID    NUMBER PRIMARY KEY,
    USERNAME   VARCHAR2(50)  NOT NULL UNIQUE,
    EMAIL      VARCHAR2(100) NOT NULL UNIQUE,
    PASSWORD   VARCHAR2(255) NOT NULL,
    CREATED_AT DATE DEFAULT SYSDATE
);
 
CREATE TABLE CATEGORIES (
    CAT_ID   NUMBER PRIMARY KEY,
    CAT_NAME VARCHAR2(50) NOT NULL UNIQUE
);
 
CREATE TABLE WORDS (
    WORD_ID    NUMBER PRIMARY KEY,
    WORD_EN    VARCHAR2(100) NOT NULL,
    WORD_KO    VARCHAR2(100) NOT NULL,
    CAT_ID     NUMBER NOT NULL,
    DIFFICULTY NUMBER(1) NOT NULL CHECK (DIFFICULTY IN (1, 2, 3)),
    CONSTRAINT FK_WORDS_CAT FOREIGN KEY (CAT_ID) REFERENCES CATEGORIES(CAT_ID)
);
 
CREATE TABLE QUIZ_SESSION (
    SESSION_ID  NUMBER PRIMARY KEY,
    USER_ID     NUMBER NOT NULL,
    STARTED_AT  DATE DEFAULT SYSDATE,
    TOTAL_SCORE NUMBER DEFAULT 0,
    CONSTRAINT FK_SESSION_USER FOREIGN KEY (USER_ID) REFERENCES USERS(USER_ID)
);
 
CREATE TABLE LEADERBOARD (
    RANK_ID     NUMBER PRIMARY KEY,
    USER_ID     NUMBER NOT NULL UNIQUE,
    TOTAL_SCORE NUMBER DEFAULT 0,
    UPDATED_AT  DATE DEFAULT SYSDATE,
    CONSTRAINT FK_RANK_USER FOREIGN KEY (USER_ID) REFERENCES USERS(USER_ID)
);
 
-- ------------------------------------------------------------
--  BƯỚC 5: TẠO TRIGGER 트리거 생성
-- ------------------------------------------------------------
CREATE OR REPLACE TRIGGER TRG_USERS
BEFORE INSERT ON USERS FOR EACH ROW
BEGIN SELECT SEQ_USERS.NEXTVAL INTO :NEW.USER_ID FROM DUAL; END;
/
 
CREATE OR REPLACE TRIGGER TRG_CATEGORIES
BEFORE INSERT ON CATEGORIES FOR EACH ROW
BEGIN SELECT SEQ_CATEGORIES.NEXTVAL INTO :NEW.CAT_ID FROM DUAL; END;
/
 
CREATE OR REPLACE TRIGGER TRG_WORDS
BEFORE INSERT ON WORDS FOR EACH ROW
BEGIN SELECT SEQ_WORDS.NEXTVAL INTO :NEW.WORD_ID FROM DUAL; END;
/
 
CREATE OR REPLACE TRIGGER TRG_SESSION
BEFORE INSERT ON QUIZ_SESSION FOR EACH ROW
BEGIN SELECT SEQ_SESSION.NEXTVAL INTO :NEW.SESSION_ID FROM DUAL; END;
/
 
CREATE OR REPLACE TRIGGER TRG_RANK
BEFORE INSERT ON LEADERBOARD FOR EACH ROW
BEGIN SELECT SEQ_RANK.NEXTVAL INTO :NEW.RANK_ID FROM DUAL; END;
/
 
-- ------------------------------------------------------------
--  BƯỚC 6: TẠO INDEX 인덱스 생성
-- ------------------------------------------------------------
CREATE INDEX IDX_WORDS_DIFFICULTY ON WORDS(DIFFICULTY);
CREATE INDEX IDX_WORDS_CAT        ON WORDS(CAT_ID);
CREATE INDEX IDX_SESSION_USER     ON QUIZ_SESSION(USER_ID);
CREATE INDEX IDX_RANK_SCORE       ON LEADERBOARD(TOTAL_SCORE DESC);
 
